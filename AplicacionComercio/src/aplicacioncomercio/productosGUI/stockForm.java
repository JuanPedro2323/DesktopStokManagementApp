    public productosBL recuperarDatosGUI(){//Todo lo que el usuario escribe
        
        
        productosBL objProductos= new productosBL();
        
        objProductos.setCodigo(txtCodigo.getText());
        
        int compras = (txtCompras.getText().isEmpty())?0: Integer.parseInt(txtCompras.getText());
        objProductos.setComprados(compras);
        int ventas = (txtVentas.getText().isEmpty())?0: Integer.parseInt(txtVentas.getText());
        objProductos.setVendidos(ventas);
            
        return objProductos;
    
    }
    
    public void mostrarDatos(){
        
        while (model1.getRowCount()>0){
            model1.removeRow(0);
        }
           try {
                conexion objConnection = new conexion();
                ResultSet resultado = objConnection.consultarRegistros("SELECT * FROM tablaStock");
                while (resultado.next()){
                System.out.println(resultado.getString("codigo"));
                
                System.out.println(resultado.getInt("comprados"));
                System.out.println(resultado.getInt("vendidos"));
                System.out.println(resultado.getInt("stock"));
                System.out.println(resultado.getString("fecha"));
            
                Object[] objInfo = {resultado.getString("codigo"), resultado.getInt("comprados"), resultado.getInt("vendidos"),resultado.getInt("stock"), resultado.getString("fecha")};
                model1.addRow(objInfo);

            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e + "Error al mostrar los datos. Reinicie el programa!");
        }
        
        
    }
    
    
    public void eliminarRegistros(){
        conexion objConnection = new conexion();
        
        int fila = tblStock.getSelectedRow();
        //int filas[] = tblProductos.getSelectedRows();
        //for (int i= 0; i< filas.length; i++){
            
        String valor = tblStock.getValueAt(fila, 0).toString(); //columna 0 o sea, código.
            
        try {
            String sentenciaInsert = String.format("DELETE FROM tablaStock WHERE codigo ='"+ valor+"'");
            objConnection.ejecutarSentenciaSQL(sentenciaInsert);
            this.mostrarDatos();
            this.limpiarRegistros();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar la fila que desea eliminar.-");
            
        }
    
    }
    
    public void modificarRegistros(){
        
        try {
            conexion objConnection = new conexion();
        
            productosBL objProductos = recuperarDatosGUI();
            
            String timeStamp = new SimpleDateFormat("dd/MM/yyyy HH:mm").format(Calendar.getInstance().getTime());
            
            String sentenciaInsert = String.format("UPDATE tablaStock SET comprados = '%d', vendidos = '%d', stock = '%d', fecha = '"+timeStamp+"' "
                    + "WHERE codigo = '%s'", objProductos.getComprados(), objProductos.getVendidos(), objProductos.getStock(),objProductos.getCodigo());
            
            String sentenciaInsertStockTablaProductos = String.format("UPDATE tablaProductos SET stock = '%d' WHERE codigo = '%s'", objProductos.getStock(), objProductos.getCodigo());
            
            objConnection.ejecutarSentenciaSQL(sentenciaInsert);
            objConnection.ejecutarSentenciaSQL(sentenciaInsertStockTablaProductos);
            this.mostrarDatos();
            this.limpiarRegistros();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Algo salió mal al actualizar la base de datos. Reinicie el programa.- ");
        }
        
       
    }  
    
