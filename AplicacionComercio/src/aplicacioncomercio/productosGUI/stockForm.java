    private void tblStockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStockMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount()==1){

            try {   
                
                JTable receptor =(JTable)evt.getSource();
            
                txtCodigo.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),0).toString());
                txtCompras.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),1).toString());
                txtVentas.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),2).toString());
                                                                                                                    //RECUPERA INFO DE TBL Y COMPLETA TXT
                try{
                    int selection = tblStock.getSelectedRow();   //Hago conversión a coordenadas de la tabla sin filtrar de la columna seleccionada, o sea 
                    if (selection>0) {                           //*(sigue) posición original!!!
                        selection = tblStock.convertRowIndexToModel(selection);
                        txtCodigo.setText(tblStock.getModel().getValueAt(selection,0).toString());
                        txtCompras.setText(tblStock.getModel().getValueAt(selection,1).toString());
                        txtVentas.setText(tblStock.getModel().getValueAt(selection,2).toString());
                    }
                } catch (Exception e) {
                    
                    //txtBuscar.setText("");
                    JOptionPane.showMessageDialog(null, "Error al seleccionar, pruebe de otra manera! :/");
                    JOptionPane.showMessageDialog(null, "1- Escriba el código. 2- Seleccione la fila requerida. 3- Borre lo escrito en el buscador. 4- Haga las modificaciones necesarias en el producto seleccionado. 5- Presione Modificar/Cancelar.");
            }
            
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "***** Error al seleccionar, pruebe de otra manera! *****");
            JOptionPane.showMessageDialog(null, "1- Escriba el código. 2- Seleccione la fila requerida. 3- Borre lo escrito en el buscador. 4- Haga las modificaciones necesarias en el producto seleccionado. 5- Presione Modificar/Cancelar.");
            }
        }
    }//GEN-LAST:event_tblStockMouseClicked

